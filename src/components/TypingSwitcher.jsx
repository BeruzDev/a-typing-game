import React from 'react'
import { IconButton } from '@mui/material'
import DataObjectIcon from '@mui/icons-material/DataObject'
import TitleIcon from '@mui/icons-material/Title'


const TypingSwitcher = ({ isAutoGenerated, toggleMode }) => {
    const handleKeyDown = (event) => {
        if (event.code === 'Space') {
            event.preventDefault(); // Previene la activaci√≥n con la barra espaciadora
        }
    }

    return (
        <div>
            <IconButton onClick={toggleMode} onKeyDown={handleKeyDown} color='inherit'>
                {isAutoGenerated ? <DataObjectIcon /> : <TitleIcon /> }
            </IconButton>
        </div>
    )
}

export default TypingSwitcher
